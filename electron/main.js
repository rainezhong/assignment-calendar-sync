// Fetch courses for selection
ipcMain.handle('fetch-courses', async (event) => {
  return new Promise((resolve) => {
    const pythonPath = getPythonPath();
    const fetchScript = path.join(pythonPath, 'fetch_courses.py');
    
    const python = spawn('python3', [fetchScript], {
      cwd: pythonPath,
      env: { ...process.env, PYTHONPATH: pythonPath }
    });

    let output = '';
    let hasError = false;

    // Send real-time output to renderer
    python.stdout.on('data', (data) => {
      const text = data.toString();
      output += text;
      event.sender.send('course-fetch-output', { type: 'stdout', data: text });
    });

    python.stderr.on('data', (data) => {
      const text = data.toString();
      output += text;
      hasError = true;
      event.sender.send('course-fetch-output', { type: 'stderr', data: text });
    });

    python.on('close', (code) => {
      if (code === 0) {
        // Try to parse course data from output
        try {
          const lines = output.split('\n');
          const dataLine = lines.find(line => line.startsWith('COURSE_DATA:'));
          if (dataLine) {
            const courseData = JSON.parse(dataLine.replace('COURSE_DATA:', ''));
            resolve({ success: true, courses: courseData });
          } else {
            resolve({ success: false, error: 'No course data found in output' });
          }
        } catch (e) {
          resolve({ success: false, error: `Failed to parse course data: ${e.message}` });
        }
      } else {
        resolve({ success: false, error: hasError ? 'Script failed with errors' : 'Script exited with error code' });
      }
      
      event.sender.send('course-fetch-complete', { success: code === 0, code, output });
    });

    python.on('error', (error) => {
      const errorMsg = `Failed to start Python process: ${error.message}`;
      resolve({ success: false, error: errorMsg });
      event.sender.send('course-fetch-complete', { success: false, error: errorMsg });
    });

    // Timeout after 5 minutes
    setTimeout(() => {
      python.kill();
      const errorMsg = 'Course fetch timed out after 5 minutes';
      resolve({ success: false, error: errorMsg });
      event.sender.send('course-fetch-complete', { success: false, error: errorMsg });
    }, 300000);
  });
});

