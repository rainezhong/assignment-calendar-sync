// Test configuration
ipcMain.handle('test-config', async (event) => {
  return new Promise((resolve) => {
    const pythonPath = getPythonPath();
    const configScript = path.join(pythonPath, 'config.py');
    
    const python = spawn('python3', [configScript], {
      cwd: pythonPath,
      env: { ...process.env, PYTHONPATH: pythonPath }
    });

    let output = '';
    let error = '';

    python.stdout.on('data', (data) => {
      output += data.toString();
    });

    python.stderr.on('data', (data) => {
      error += data.toString();
    });

    python.on('close', (code) => {
      if (code === 0) {
        resolve({ success: true, output });
      } else {
        resolve({ success: false, error: error || 'Configuration test failed' });
      }
    });

    // Timeout after 30 seconds
    setTimeout(() => {
      python.kill();
      resolve({ success: false, error: 'Configuration test timed out' });
    }, 30000);
  });
});

// Run the simple sync process
ipcMain.handle('run-sync', async (event, options = {}) => {
  if (pythonProcess) {
    pythonProcess.kill();
    pythonProcess = null;
  }

  return new Promise((resolve) => {
    const pythonPath = getPythonPath();
    const syncScript = path.join(pythonPath, 'combined_sync.py');
    
    // Use combined_sync.py for ICS + Notion Calendar sync
    pythonProcess = spawn('python3', [syncScript], {
      cwd: pythonPath,
      env: { ...process.env, PYTHONPATH: pythonPath }
    });

    let output = '';
    let hasError = false;

    // Send real-time output to renderer
    pythonProcess.stdout.on('data', (data) => {
      const text = data.toString();
      output += text;
      event.sender.send('sync-output', { type: 'stdout', data: text });
    });

    pythonProcess.stderr.on('data', (data) => {
      const text = data.toString();
      output += text;
      hasError = true;
      event.sender.send('sync-output', { type: 'stderr', data: text });
    });

    pythonProcess.on('close', (code) => {
      pythonProcess = null;
      
      if (code === 0) {
        resolve({ success: true, output, code });
      } else {
        resolve({ success: false, output, code, error: hasError });
      }
      
      event.sender.send('sync-complete', { success: code === 0, code, output });
    });

    pythonProcess.on('error', (error) => {
      pythonProcess = null;
      const errorMsg = `Failed to start Python process: ${error.message}`;
      resolve({ success: false, error: errorMsg });
      event.sender.send('sync-complete', { success: false, error: errorMsg });
    });

    // Timeout after 10 minutes
    setTimeout(() => {
      if (pythonProcess) {
        pythonProcess.kill();
        pythonProcess = null;
        const errorMsg = 'Sync process timed out after 10 minutes';
        resolve({ success: false, error: errorMsg });
        event.sender.send('sync-complete', { success: false, error: errorMsg });
      }
    }, 600000);
  });
});

// Cancel running sync
ipcMain.handle('cancel-sync', async (event) => {
  if (pythonProcess) {
    pythonProcess.kill();
    pythonProcess = null;
    return { success: true, message: 'Sync cancelled' };
  }
  return { success: false, message: 'No sync process running' };
});

