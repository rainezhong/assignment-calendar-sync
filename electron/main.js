const { app, BrowserWindow, ipcMain, dialog, shell } = require('electron');
const { spawn, exec } = require('child_process');
const path = require('path');
const fs = require('fs');
const os = require('os');

// Simple JSON-based settings storage
const settingsPath = path.join(os.homedir(), '.assignment-calendar-sync.json');

function loadSettings() {
  try {
    if (fs.existsSync(settingsPath)) {
      return JSON.parse(fs.readFileSync(settingsPath, 'utf8'));
    }
  } catch (error) {
    console.error('Error loading settings:', error);
  }
  return {
    gradescopeUseSSO: true,
    gradescopeEmail: '',
    gradescopePassword: '',
    syncDaysAhead: 30,
    timezone: 'America/New_York',
    firstRun: true
  };
}

function saveSettings(settings) {
  try {
    fs.writeFileSync(settingsPath, JSON.stringify(settings, null, 2));
    return true;
  } catch (error) {
    console.error('Error saving settings:', error);
    return false;
  }
}

let mainWindow;
let pythonProcess = null;
let isDev = process.env.ELECTRON_IS_DEV === '1' || !app.isPackaged;

// Get the correct path for Python scripts
function getPythonPath() {
  if (isDev) {
    return path.join(__dirname, '..', 'python');
  } else {
    // In production, look for bundled Python first, then fall back to system
    const bundledPath = path.join(process.resourcesPath, 'python');
    if (fs.existsSync(bundledPath)) {
      return bundledPath;
    }
    // Fall back to same directory as executable
    return path.join(path.dirname(process.execPath), 'python');
  }
}

// Create .env file for Python scripts
function createEnvFile(settings) {
  const pythonPath = getPythonPath();
  
  console.log(`Python path detected as: ${pythonPath}`);
  console.log(`isDev: ${isDev}`);
  console.log(`app.isPackaged: ${app.isPackaged}`);
  
  // Check if Python directory exists
  if (!fs.existsSync(pythonPath)) {
    console.error(`Python directory does not exist: ${pythonPath}`);
    return false;
  }
  
  const envPath = path.join(pythonPath, '.env');
  
  const envContent = [
    '# Assignment Calendar Sync Configuration',
    '# Generated by Desktop App',
    '',
    `GRADESCOPE_USE_SSO=${settings.gradescopeUseSSO}`,
    `GRADESCOPE_EMAIL=${settings.gradescopeEmail || ''}`,
    `GRADESCOPE_PASSWORD=${settings.gradescopePassword || ''}`,
    '',
    `NOTION_API_TOKEN=${settings.notionToken || ''}`,
    `NOTION_DATABASE_ID=${settings.notionDatabase || ''}`,
    '',
    `SYNC_DAYS_AHEAD=${settings.syncDaysAhead || 30}`,
    `TARGET_SEMESTER=${settings.targetSemester || 'current'}`,
    `DEFAULT_REMINDER_MINUTES=60`,
    `HEADLESS_BROWSER=false`,
    `DEBUG_MODE=false`,
    `TIMEZONE=${settings.timezone || 'America/New_York'}`,
    '',
    '# Not needed for ICS mode',
    'GOOGLE_CLIENT_ID=',
    'GOOGLE_CLIENT_SECRET=',
    'GOOGLE_CALENDAR_ID=primary'
  ].join('\n');
  
  try {
    fs.writeFileSync(envPath, envContent);
    console.log(`Successfully created .env file at: ${envPath}`);
    return true;
  } catch (error) {
    console.error('Error creating .env file:', error);
    return false;
  }
}

function createWindow() {
  // Create the browser window
  mainWindow = new BrowserWindow({
    width: 1000,
    height: 800,
    minWidth: 900,
    minHeight: 700,
    webPreferences: {
      nodeIntegration: true,
      contextIsolation: false,
      enableRemoteModule: true
    },
    titleBarStyle: 'default',
    show: false,
    icon: path.join(__dirname, '../assets/icon.png') // We'll create this
  });

  // Load the simple interface
  mainWindow.loadFile('renderer/simple.html');

  // Show window when ready
  mainWindow.once('ready-to-show', () => {
    mainWindow.show();
    
    if (isDev) {
      mainWindow.webContents.openDevTools();
    }
  });

  // Handle window closed
  mainWindow.on('closed', () => {
    mainWindow = null;
    // Kill Python process if running
    if (pythonProcess) {
      pythonProcess.kill();
      pythonProcess = null;
    }
  });
}

// App event handlers
app.whenReady().then(createWindow);

app.on('window-all-closed', () => {
  // On macOS, keep app running even when all windows are closed
  if (process.platform !== 'darwin') {
    app.quit();
  }
});

app.on('activate', () => {
  // On macOS, re-create window when dock icon is clicked
  if (BrowserWindow.getAllWindows().length === 0) {
    createWindow();
  }
});

app.on('before-quit', () => {
  // Clean up Python process
  if (pythonProcess) {
    pythonProcess.kill();
  }
});

